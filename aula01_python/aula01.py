# Exercício 01
'''
Crie um programa que o usuário diga o seu nome e retorna o número de caracteres
'''

nome = input("Digite o seu nome: ")
caracteres_nome = len(nome)

print(f"O seu nome tem {caracteres_nome} caracteres")

# Exercício 02
'''
Crie um programa onde o usuário digite dois valores e apareça a soma
'''

valor_01 = int(input("Digite o primeiro valor: "))
valor_02 = int(input("Digite o segundo valor: "))
soma_valores = valor_01 + valor_02

print(f"A soma dos valores é {soma_valores}")